/* USER CODE BEGIN 4 */
void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	static int i = 0;

	if(htim == &htim1)
	{
		//HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);

		if(sine[i]>0)
		{
			TIM1->CCR1 = sine[i];
			TIM1->CCR2 = 0;
		}
		else
		{
			TIM1->CCR1 = 0;
			TIM1->CCR2 = -sine[i];
		}

		if(++i == DATA_SIZE)
			i = 0;
	}
}
